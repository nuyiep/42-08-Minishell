#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = -1;
	if (!str)
		return (NULL);
	while (str[++i] != '\0')
		if (str[i] == (char)c)
			return ((char *)(str + i));
	if (str[i] == (char)c)
		return ((char *)(str + i));
	return (NULL);
}
